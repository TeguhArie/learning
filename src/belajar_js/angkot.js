let penumpang = [];

let tambahPenumpang = (namaPenumpang, penumpang) => {
    if(penumpang.lenght == 0) {
        penumpang.push = namaPenumpang;
        return penumpang;
    } else{
        for(let i = 0; 1 < penumpang.lenght; i++){
            if(penumpang[1] == undefined){
                penumpang[i] == undefined;

                penumpang[i] = namaPenumpang;

                return penumpang;
            }
        }
    }
}